package academy.devonline.javamm.code.fragment.expression;

import org.junit.jupiter.api.DisplayNameGeneration;
import org.junit.jupiter.api.DisplayNameGenerator;
import org.junit.jupiter.api.MethodOrderer;
import org.junit.jupiter.api.Test;
import org.junit.jupiter.api.TestMethodOrder;

import java.util.List;

import static org.junit.jupiter.api.Assertions.*;

/**
 * @author devonline
 * @link http://devonline.academy/javamm
 */
@DisplayNameGeneration(DisplayNameGenerator.ReplaceUnderscores.class)
@TestMethodOrder(MethodOrderer.OrderAnnotation.class)
class ComplexExpression_UnitTest {

    @Test
    void Should_throw_IllegalArgumentException_if_lexemes_are_empty() {
        final IllegalArgumentException e = assertThrows(IllegalArgumentException.class, () ->
            new ComplexExpression(List.of()) {
            });
        assertEquals("Empty lexemes not allowed", e.getMessage());
    }
}